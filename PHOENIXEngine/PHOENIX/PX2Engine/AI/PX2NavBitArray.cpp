// PX2NavBitArray.cpp

#include "PX2NavBitArray.hpp"
using namespace PX2;

//----------------------------------------------------------------------------
// BitArray1D
//----------------------------------------------------------------------------
BitArray1D::BitArray1D(int len) : 
m_NumBits(len)
{
	m_BufLen = (m_NumBits + 31) / 32;
	m_pData = new unsigned int[m_BufLen];
}
//----------------------------------------------------------------------------
BitArray1D::~BitArray1D()
{
	delete[] m_pData;
}
//----------------------------------------------------------------------------

//----------------------------------------------------------------------------
// BitArray2D
//----------------------------------------------------------------------------
BitArray2D::BitArray2D(int nbitx, int nbity) :
mBitX(nbitx), mBitY(nbity), mBitArray(nbitx*nbity)
{
}
//----------------------------------------------------------------------------
BitArray2D::~BitArray2D()
{
}
//----------------------------------------------------------------------------
void BitArray2D::Clear()
{
	mBitArray.Clear();
}
//----------------------------------------------------------------------------
int BitArray2D::GetBit(int x, int y)
{
	return mBitArray.GetBit(y*mBitX + x);
}
//----------------------------------------------------------------------------
void BitArray2D::SetBit(int x, int y, int bit)
{
	mBitArray.SetBit(y*mBitX + x, bit);
}

